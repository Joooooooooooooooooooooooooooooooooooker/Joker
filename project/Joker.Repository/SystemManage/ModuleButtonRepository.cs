using Joker.Data;
using Joker.Domain.Entity.SystemManage;
using Joker.Domain.IRepository.SystemManage;
using Joker.Repository.SystemManage;
using System.Collections.Generic;

namespace Joker.Repository.SystemManage
{
    public class ModuleButtonRepository : RepositoryBase<ModuleButtonEntity>, IModuleButtonRepository
    {
        public void SubmitCloneButton(List<ModuleButtonEntity> entitys)
        {
            using (var db = new RepositoryBase().BeginTrans())
            {
                foreach (var item in entitys)
                {
                    db.Insert(item);
                }
                db.Commit();
            }
        }
    }
}
